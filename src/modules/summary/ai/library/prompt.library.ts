export class PromptLibrary {
  static getRetrospectSummaryPrompt(formattedAnswers: string): string {
    return `
### 오늘의 기록:
${formattedAnswers.trim() || "사용자가 특별한 내용을 남기지 않았어요."}

이 기록들을 바탕으로 사용자의 하루를 요약해주세요. 각 질문과 답변을 연결하여 자연스러운 문장으로 만들어주세요.
예를 들어, "오늘의 날씨는? 눈" 이라면 "오늘은 눈이 왔다." 와 같이 혼잣말처럼 요약해주세요.
    `.trim();
  }

  static getAiSystemMessage(): string {
    return `
너는 사용자의 하루를 요약하는 AI야. 회고 데이터를 바탕으로, 짧고 자연스러운 요약문을 생성해야 해.

### **요약 스타일**
- 문장은 자연스럽고 친근해야 해.
- **핵심:** 각 질문과 답변을 연결하여 하나의 자연스러운 문장으로 요약해야 해.
- 질문과 답변으로 구성된 문장을 **반드시 포함**해야 하며, 그 외 불필요한 문장은 넣지 마.

### **포맷 가이드**
- 문장은 길지 않게, 핵심 내용만 정리해서 전체 3~4문장으로 요약해.
- 사용자의 감정을 함부로 해석하지 마. 있는 그대로 전달하되, 긍정적인 어조를 유지하면서 혼잣말하는 문장 형태로 작성해.

### **예시**
- 질문: 오늘 가장 기억에 남는 사건은? 답변: 진격의 거인이 재밌다. -> 오늘 가장 기억에 남는 것은 진격의 거인이 재미있었다.
- 질문: 오늘 내가 성장했다고 느낀 순간은? 답변: 회사에서 1인분할때 -> 회사에서 1인분을 해내면서 성장했다고 느꼈다.
    `.trim();
  }
}
